import { useState } from 'react'
import { Download, CheckCheck } from 'lucide-react'

function toMarkdown(result) {
  const score = Math.round(result.overall_risk_score * 100)
  let md = `# Blast Radius Analysis: ${result.decision_title}\n\n`
  md += `**Verdict:** ${result.overall_verdict} | **Risk Score:** ${score}/100\n\n`
  md += `## Executive Summary\n\n${result.executive_summary}\n\n`
  md += `## Risk Dimensions\n\n| Dimension | Score | Summary |\n|---|---|---|\n`
  for (const d of result.risk_dimensions) {
    md += `| ${d.dimension} | ${Math.round(d.score * 100)} | ${d.summary} |\n`
  }
  md += `\n## Immediate Actions\n\n`
  result.immediate_actions.forEach((a, i) => { md += `${i + 1}. ${a}\n` })
  md += `\n## Open Questions\n\n`
  result.questions_to_answer.forEach(q => { md += `- [ ] ${q}\n` })
  md += `\n## Impact Nodes\n\n| Node | Type | Impact | Severity | Inferred |\n|---|---|---|---|---|\n`
  for (const n of result.nodes) {
    md += `| ${n.name} | ${n.type} | ${n.impact_level} | ${n.risk_severity} | ${n.is_inferred ? '◈' : ''} |\n`
  }
  md += `\n---\n*Generated by [Blast Radius Analyzer](https://github.com/your-org/blast-radius-analyzer)*\n\n`
  md += `> ${result.confidence_note}\n`
  return md
}

export default function ExportButton({ result }) {
  const [copied, setCopied] = useState(false)
  const handle = () => {
    navigator.clipboard.writeText(toMarkdown(result))
    setCopied(true)
    setTimeout(() => setCopied(false), 3000)
  }
  return (
    <button
      onClick={handle}
      className="flex items-center gap-2 px-4 py-2 border border-border rounded-lg text-sm text-text-secondary hover:text-text-primary hover:border-text-secondary transition-colors"
    >
      {copied
        ? <><CheckCheck size={14} className="text-low" /><span className="text-low">Copied!</span></>
        : <><Download size={14} /><span>Copy as Markdown</span></>}
    </button>
  )
}
